CATEGORIES = [
    "성향·기질",
    "가치관·태도",
    "생활·일상",
    "취미·관심사",
    "소통·관계",
]


@dataclass(frozen=True)
class KeywordEntry:
    category: str
    text: str
    normalized_ws: str  # 공백 제거
    normalized_plain: str  # 공백 + 기호/이모지 제거


def _normalize_ws(text: str) -> str:
    return re.sub(r"\s+", "", text.lower())


def _normalize_plain(text: str) -> str:
    # 한글/영문/숫자만 남기고 나머지 제거 (이모지/기호 제거)
    return re.sub(r"[^0-9a-zA-Zㄱ-ㅎ가-힣]+", "", text.lower())


def _parse_raw_keywords(raw: str) -> List[KeywordEntry]:
    entries: List[KeywordEntry] = []
    seen: set[Tuple[str, str]] = set()
    for line in raw.splitlines():
        line = line.strip()
        if not line or line.startswith("카테고리"):
            continue
        # 기본: 탭 기준 분리
        if "\t" in line:
            category, text = line.split("\t", 1)
        else:
            # 보정: 카테고리 접합 케이스 처리
            category = None
            text = None
            for cat in CATEGORIES:
                if line.startswith(cat):
                    category = cat
                    text = line[len(cat):].strip()
                    break
            if category is None or text is None:
                continue
        category = category.strip()
        text = text.strip()
        if not category or not text:
            continue
        key = (category, text)
        if key in seen:
            continue
        seen.add(key)
        normalized_ws = _normalize_ws(text)
        normalized_plain = _normalize_plain(text)
        entries.append(
            KeywordEntry(
                category=category,
                text=text,
                normalized_ws=normalized_ws,
                normalized_plain=normalized_plain,
            )
        )
    return entries


